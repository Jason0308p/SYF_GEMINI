-- The `USE` command is specific to MySQL and some other database systems.
-- It is used to select a database to work with.

-- In PostgreSQL, you connect to a specific database when you establish the connection.
-- There is no direct equivalent of the `USE` command to switch databases within a session.
-- To switch databases, you would typically end your current connection and start a new one.

-- In psql (the PostgreSQL command-line tool), you can use the `\c` command to connect to a different database:

-- \c company_db
